    # Helper methods

    def _add_frontmatter(self, content: str, metadata: dict) -> str:
        """Add YAML frontmatter to content."""
        yaml_lines = ["---"]
        for key, value in metadata.items():
            if isinstance(value, list):
                yaml_lines.append(f"{key}: [{', '.join(str(v) for v in value)}]")
            elif isinstance(value, str):
                yaml_lines.append(f'{key}: "{value}"')
            else:
                yaml_lines.append(f"{key}: {value}")
        yaml_lines.append("---")
        yaml_lines.append("")

        return "\n".join(yaml_lines) + content

    def _add_generated_marker(self, content: str, source: Path) -> str:
        """Add generated marker to content."""
        marker = f"{self.GENERATED_MARKER}\n"
        marker += f"<!-- Source: {source} | Last generated: {datetime.now(timezone.utc).isoformat()} -->\n\n"
        return marker + content

    def _convert_file_refs_to_wikilinks(self, content: str) -> str:
        """Convert file path references to wiki-links."""
        # Convert explicit mappings
        for path, wikilink in self.WIKILINK_MAPPINGS.items():
            content = content.replace(f"`{path}`", f"[[{wikilink}]]")

        # Convert common patterns
        content = re.sub(r"`spec\.md`", "[[spec]]", content)
        content = re.sub(r"`tasks\.md`", "[[tasks]]", content)
        content = re.sub(r"`decisions\.md`", "[[decisions]]", content)
        content = re.sub(r"`state\.json`", "`state.json`", content)  # Keep state.json as-is

        return content

    def _count_tasks(self, content: str) -> dict:
        """Count task statuses from tasks.md content."""
        return {
            "total_tasks": len(re.findall(r"- status:", content)),
            "pending": len(re.findall(r"- status: \[ \]", content)),
            "completed": len(re.findall(r"- status: \[x\]", content)),
            "blocked": len(re.findall(r"- status: \[!\]", content)),
            "in_progress": len(re.findall(r"- status: \[~\]", content)),
        }

    def _load_state(self, change_dir: Path) -> dict | None:
        """Load state.json from change directory."""
        state_file = change_dir / "state.json"
        if not state_file.exists():
            return None
        try:
            return json.loads(state_file.read_text())
        except json.JSONDecodeError:
            return None

    def _write_file(self, path: Path, content: str):
        """Write file with dry-run support."""
        if self.dry_run:
            print(f"  [dry-run] Would write: {path}")
        else:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content)

    def _write_json(self, path: Path, data: dict):
        """Write JSON file with dry-run support."""
        if self.dry_run:
            print(f"  [dry-run] Would write JSON: {path}")
        else:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(json.dumps(data, indent=2))


