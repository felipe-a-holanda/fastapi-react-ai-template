#!/usr/bin/env python3
"""
Generate Obsidian vault from FORGE documents.

This script transforms FORGE's agent-optimized markdown files into an Obsidian vault
with wiki-links, YAML frontmatter, MOCs, graph styling, and dataview queries - without
modifying any source FORGE documents.

Usage:
    python forge/scripts/generate_vault.py --output .forge-vault
    python forge/scripts/generate_vault.py --output ~/Documents/ObsidianVaults/forge
    python forge/scripts/generate_vault.py --output .forge-vault --force
    python forge/scripts/generate_vault.py --output .forge-vault --dry-run
"""

import argparse
import json
import re
import shutil
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


class VaultGenerator:
    """Generate Obsidian vault from FORGE documents."""

    GENERATED_MARKER = "<!-- GENERATED by forge/scripts/generate_vault.py - DO NOT EDIT -->"

    # Wiki-link mappings for file path conversions
    WIKILINK_MAPPINGS = {
        "forge/FORGE.md": "FORGE-Protocol",
        "forge/CLAUDE.md": "CLAUDE-Manual",
        "forge/global/architecture.md": "Architecture",
        "forge/global/constraints.md": "Constraints",
        "forge/global/verification.md": "Verification",
        "AGENTS.md": "AGENTS",
        "ARCHITECTURE.md": "ARCHITECTURE",
    }

    def __init__(self, forge_root: Path, output_path: Path, force: bool = False, dry_run: bool = False):
        self.forge_root = forge_root
        self.output_path = output_path
        self.force = force
        self.dry_run = dry_run
        self.changes_dir = forge_root / "forge" / "changes"
        self.global_dir = forge_root / "forge" / "global"
        self.forge_dir = forge_root / "forge"

    def generate(self):
        """Main generation orchestrator."""
        print(f"\n🔨 FORGE Obsidian Vault Generator")
        print(f"   Source: {self.forge_root}")
        print(f"   Output: {self.output_path}")
        print(f"   Mode: {'DRY RUN' if self.dry_run else 'FORCE' if self.force else 'SMART MERGE'}\n")

        # Validate FORGE structure
        if not self._validate_forge_structure():
            print("✗ Invalid FORGE structure. Aborting.")
            sys.exit(1)

        # Handle force mode
        if self.force and self.output_path.exists() and not self.dry_run:
            print(f"🗑  Force mode: removing existing vault at {self.output_path}")
            shutil.rmtree(self.output_path)

        # Create vault directories
        self._create_vault_structure()

        # Generate .obsidian config
        self._generate_obsidian_config()

        # Transform core docs
        self._transform_core_docs()

        # Transform changes
        self._transform_changes()

        # Generate MOCs
        self._generate_mocs()

        # Generate canvas
        self._generate_canvas()

        # Create templates (if missing)
        self._create_templates()

        print(f"\n✓ Vault generation complete!")
        if not self.dry_run:
            print(f"  Open {self.output_path} in Obsidian to explore.")

    def _validate_forge_structure(self) -> bool:
        """Validate that FORGE structure exists."""
        required = [
            self.forge_dir / "FORGE.md",
            self.forge_dir / "CLAUDE.md",
            self.global_dir / "architecture.md",
            self.global_dir / "constraints.md",
            self.global_dir / "verification.md",
            self.changes_dir,
        ]
        for path in required:
            if not path.exists():
                print(f"✗ Missing required file: {path}")
                return False
        return True

    def _create_vault_structure(self):
        """Create vault directory structure."""
        dirs = [
            self.output_path / ".obsidian",
            self.output_path / ".obsidian" / "plugins",
            self.output_path / ".obsidian" / "snippets",
            self.output_path / "_meta",
            self.output_path / "_templates",
            self.output_path / "_canvas",
            self.output_path / "_custom",
            self.output_path / "core",
            self.output_path / "changes",
        ]
        for d in dirs:
            if self.dry_run:
                print(f"  [dry-run] Would create: {d}")
            else:
                d.mkdir(parents=True, exist_ok=True)
                print(f"  ✓ Created: {d}")

